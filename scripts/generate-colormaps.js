// ---------------------------------------------------------------------------
// Programmatically generated colormaps
// ---------------------------------------------------------------------------

// Grayscale (inverted): white → black (silence is white, loud is black)
// Matches the Xeno-Canto / scientific spectrogram style.
COLORMAPS.grayscale = Array.from({ length: 256 }, (_, i) => {
  const v = 255 - i;
  return [v, v, v];
});

// ---------------------------------------------------------------------------
// Packing: RGB -> RGBA Uint32 for little-endian ImageData
// Layout in memory: [R, G, B, A] at byte level
// As Uint32 on little-endian: (R) | (G << 8) | (B << 16) | (A << 24)
// ---------------------------------------------------------------------------

const ALPHA = 255;

/**
 * Pack an [R, G, B] triple into a little-endian RGBA Uint32 value.
 */
function packRGBA(r, g, b) {
  return (r | (g << 8) | (b << 16) | (ALPHA << 24)) >>> 0;
}

// ---------------------------------------------------------------------------
// Validation
// ---------------------------------------------------------------------------

for (const [name, entries] of Object.entries(COLORMAPS)) {
  if (entries.length !== 256) {
    console.error(`ERROR: ${name} has ${entries.length} entries, expected 256`);
    process.exit(1);
  }
  for (let i = 0; i < entries.length; i++) {
    const [r, g, b] = entries[i];
    if (r < 0 || r > 255 || g < 0 || g > 255 || b < 0 || b > 255) {
      console.error(`ERROR: ${name}[${i}] has out-of-range value: [${r},${g},${b}]`);
      process.exit(1);
    }
  }
}

// ---------------------------------------------------------------------------
// Generate TypeScript output
// ---------------------------------------------------------------------------

const NAMES = Object.keys(COLORMAPS);
const ENTRIES_PER_LINE = 6;

let output = `// AUTO-GENERATED from matplotlib colormaps — do not edit manually
// Run: node scripts/generate-colormaps.js
// Source: matplotlib perceptually uniform colormaps (CC0 / MIT)
// Data: https://github.com/BIDS/colormap

export type ColorMapName = ${NAMES.map((n) => `'${n}'`).join(' | ')};

/** Precomputed RGBA lookup tables for spectrogram rendering.
 *  Index = byte magnitude (0-255 from getByteFrequencyData).
 *  Value = packed RGBA for little-endian ImageData Uint32Array view. */
export const COLOR_MAPS: Record<ColorMapName, Uint32Array> = {
`;

for (const name of NAMES) {
  const entries = COLORMAPS[name];
  const packed = entries.map(([r, g, b]) => packRGBA(r, g, b));

  output += `  ${name}: new Uint32Array([\n`;

  for (let i = 0; i < packed.length; i += ENTRIES_PER_LINE) {
    const slice = packed.slice(i, i + ENTRIES_PER_LINE);
    const line = slice.map((v) => `0x${v.toString(16).padStart(8, '0')}`).join(', ');
    const isLast = i + ENTRIES_PER_LINE >= packed.length;
    output += `    ${line}${isLast ? '' : ','}\n`;
  }

  output += `  ]),\n`;
}

output += `};

export const DEFAULT_COLOR_MAP: ColorMapName = 'inferno';
`;

// ---------------------------------------------------------------------------
// Write output file
// ---------------------------------------------------------------------------

const outPath = path.resolve(__dirname, '..', 'frontend', 'src', 'lib', 'utils', 'spectrogramColorMaps.ts');

fs.writeFileSync(outPath, output, 'utf8');

// Verify the generated file
const written = fs.readFileSync(outPath, 'utf8');
for (const name of NAMES) {
  const match = written.match(new RegExp(`${name}:\\s*new Uint32Array\\(\\[([^\\]]+)\\]\\)`));
  if (!match) {
    console.error(`ERROR: Could not find ${name} array in output`);
    process.exit(1);
  }
  const count = match[1].split(',').filter((s) => s.trim().length > 0).length;
  if (count !== 256) {
    console.error(`ERROR: ${name} has ${count} packed entries, expected 256`);
    process.exit(1);
  }
}

console.log(`Generated ${outPath}`);
console.log(`Colormaps: ${NAMES.join(', ')} (256 entries each)`);
